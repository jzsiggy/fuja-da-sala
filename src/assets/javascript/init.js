const createRow = (arrayOfBlocks) => {
    let width = window.innerWidth;

    let roomSpace = document.querySelector('#room-space');

    let row = document.createElement('div');
    row.style.display = "flex";
    row.classList.add("row");
    
    arrayOfBlocks.forEach(blockNumber => {
        let blockWidth = width / arrayOfBlocks.length;
        let element = document.createElement('div');

        element.style.width = `${blockWidth}px`;
        element.style.height = `${blockWidth}px`;
        element.classList.add("block");
        element.classList.add(`${tradutor[blockNumber]["class"]}`);

        row.appendChild(element)
    });

    roomSpace.appendChild(row);
}

