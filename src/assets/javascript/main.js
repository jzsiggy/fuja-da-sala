let createRow = (arrayOfBlocks) => {
    let width = window.innerWidth;

    let body = document.querySelector('body');

    let row = document.createElement('div');
    row.style.display = "flex";
    
    arrayOfBlocks.forEach(blockNumber => {
        let blockWidth = width / arrayOfBlocks.length;
        let path = `./assets/images/${tradutor[blockNumber]}`;
        let element = document.createElement('div');

        element.style.width = `${blockWidth}px`;
        element.style.height = `${blockWidth}px`;
        element.style.backgroundImage = `url(${path})`;
        element.style.backgroundSize = "contain";
        element.style.backgroundRepeat = "no-repeat";

        row.appendChild(element)
    });

    body.appendChild(row);
}

gameSpace.forEach(row => {
    createRow(row);
});

let moveAvatar = (e) => {
    if (e.key == "w") {
        avatar.moveUp();
    } else if (e.key == "s") {
        avatar.moveDown();
    } else if (e.key == "d") {
        avatar.moveRight();
    } else {
        avatar.moveLeft();
    }
};

document.addEventListener('keypress', moveAvatar);